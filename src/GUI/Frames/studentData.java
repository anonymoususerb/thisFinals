    private void clearTable() {
        if (model == null) {
            System.out.println("âš  Table model is null, cannot clear table!");
            return;
        }

        model.setRowCount(0); // Clears existing rows
        System.out.println("ðŸ—‘ Clearing table and adding students: " + stu.size());

        for (studyante s : stu) {
            System.out.println("ðŸ“Œ Adding Student: " + s.getStuID() + " | " + s.getFullName() + " | " + s.getCourse());
            model.addRow(new Object[]{s.getStuID(), s.getFullName(), s.getGender(), s.getCourse(), s.getYearLvl(), s.getStatus()});
        }
    }

    private void saveStudentData() {
        try (ObjectOutputStream oos = new ObjectOutputStream(new FileOutputStream("students.dat"))) {
            oos.writeObject(stu);
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

    public void refreshData(ArrayList<studyante> updatedStudents) {
        stu = updatedStudents;
        clearTable();
    }

    private void loadStudentData() {
        try (ObjectInputStream ois = new ObjectInputStream(new FileInputStream("students.dat"))) {
            stu = (ArrayList<studyante>) ois.readObject();
            System.out.println("âœ… Student data loaded successfully! Total students: " + stu.size());

            for (studyante s : stu) {
                System.out.println("ðŸ“Œ Loaded Student: " + s.getStuID() + " | " + s.getFullName() + " | " + s.getCourse());
            }
        } catch (IOException | ClassNotFoundException e) {
            System.out.println("âš  No previous data found or error loading data.");
            stu = new ArrayList<>(); // Ensure stu is not null
        }

        clearTable(); // âœ… Ensure table updates AFTER data load

        if (statsPanel != null) {
            System.out.println("ðŸ“Š Updating statistics panel...");
            statsPanel.refreshData(stu); // âœ… Now updating AFTER data load
        } else {
            System.out.println("âš  Warning: statsPanel is null, cannot refresh statistics!");
        }
    }

